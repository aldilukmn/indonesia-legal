function getNews(id) {
  if (!id) {
    return news;
  }
  const filterNews = news.filter((news) => news.id === id);

  if (!filterNews.length) {
    return null;
  }
  return filterNews[0];
}

function getEvent(id) {
  if (!id) {
    return event;
  }
  const filterEvent = event.filter((event) => event.id === id);

  if (!filterEvent.length) {
    return null;
  }
  return filterEvent[0];
}

function formatDate(dateStr) {
  const [startDate, endDate] = dateStr.split("/");
  
  const startDay = new Date(startDate).getDate();
  const endDay = new Date(endDate).getDate();
  
  const startMonth = new Date(startDate).toLocaleString("en-us", {
    month: "long",
  });
  
  const year = new Date(startDate).getFullYear();
  
  return `${startDay}-${endDay} ${startMonth} ${year}`;
}

